            <td class="bodyText"><h3><em>Long and short-term environmental changes affect and  modify complex biological systems on a multitude of levels. From a strictly  anthropocentric standpoint the understanding of such processes can assist in  perceiving the current changes our ecosystems are undergoing. Our own habitat  has entered an unfamiliar zone in terms of global carbon biogeochemical cycles,  as atmospheric CO2 levels rose for the first time in 420,000 years  above 280 ppmv. These changes are bound to have significant effects on the  global climate as the deviation from the glacial interglacial steady state  increases. I have chosen to study two systems, corals and marine phytoplankton,  which may assist in elucidating complex systems reactions to environmental  shifts. My choice to study these systems is due to their evident responses to  environmental changes and their abundance throughout the geological record, thus  providing contemporary physiologically measurable subjects that can be  manipulated in the laboratory or tested in-situ. Additionally, these two  systems leave behind skeletons that are harboring possible markers that can uncover  past climatic events. Both systems have a substantial fossil record that can  help unravel the evolutionary changes that occurred following these events. Yet  another benefit arises from investigating these two systems simultaneously; a  better understanding of the physiology of phytoplankton increases the chance of  correctly interpreting data from the much more complex coral symbiosis.</em></h3>
<p><em><strong>My overarching research aspiration is to enhance our  understanding of the fundamental effects of global climatic changes on the marine  environment, through coral and phytoplankton  systems. </strong></em> </p>
